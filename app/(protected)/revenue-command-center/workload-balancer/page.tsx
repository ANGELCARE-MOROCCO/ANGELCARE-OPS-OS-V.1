import AppShell, { PageAction } from '@/app/components/erp/AppShell'
import { createClient } from '@/lib/supabase/server'
import { ActionLink, Badge, EmptyState, Kpi, LoadCard, Panel, formatDate, statusTone } from '../_components/WorkloadBalancerPrimitives'
import { approveRebalance, closeManagerIntervention, createManagerIntervention, rejectRebalance, runWorkloadAnalysis } from './actions'

export default async function WorkloadBalancerPage() {
  const supabase = await createClient()

  const [{ data: users }, { data: snapshotsRaw }, { data: recsRaw }, { data: interventionsRaw }] = await Promise.all([
    supabase.from('app_users').select('id, full_name, username, role'),
    supabase.from('bd_workload_snapshots').select('*').order('created_at', { ascending: false }).limit(100),
    supabase.from('bd_rebalance_recommendations').select('*').eq('status', 'pending').order('created_at', { ascending: false }),
    supabase.from('bd_manager_interventions').select('*').neq('status', 'closed').order('created_at', { ascending: false }),
  ])

  const allUsers = users || []
  const latestByUser = new Map<string, any>()
  for (const s of snapshotsRaw || []) {
    if (!latestByUser.has(s.user_id)) latestByUser.set(s.user_id, s)
  }

  const snapshots = Array.from(latestByUser.values()).sort((a, b) => Number(b.weighted_pressure || 0) - Number(a.weighted_pressure || 0))
  const recs = recsRaw || []
  const interventions = interventionsRaw || []

  function userName(id?: string) {
    const user = allUsers.find((u: any) => u.id === id)
    return user?.full_name || user?.username || 'Unassigned'
  }

  const critical = snapshots.filter((s: any) => s.workload_status === 'critical')
  const high = snapshots.filter((s: any) => s.workload_status === 'high')
  const low = snapshots.filter((s: any) => s.workload_status === 'low')
  const avgPressure = snapshots.length ? Math.round(snapshots.reduce((sum: number, s: any) => sum + Number(s.weighted_pressure || 0), 0) / snapshots.length) : 0

  return (
    <AppShell
      title="Workload Auto-Balancer"
      subtitle="Tier 2 manager system for detecting overload, recommending reassignment, and creating interventions."
      breadcrumbs={[{ label: 'Revenue Command', href: '/revenue-command-center' }, { label: 'Workload Balancer' }]}
      actions={<><PageAction href="/revenue-command-center/control-tower" variant="light">Control Tower</PageAction><PageAction href="/revenue-command-center/daily-desk">Daily Desk</PageAction></>}
    >
      <div style={{ display: 'grid', gap: 18 }}>
        <section style={heroStyle}>
          <div>
            <div style={eyebrowStyle}>TIER 2 — WORKLOAD AUTO-BALANCER</div>
            <h1 style={heroTitleStyle}>Balance execution load before agents collapse.</h1>
            <p style={heroTextStyle}>Analyze pressure, detect overloaded users, recommend reassignment, and create manager interventions.</p>
          </div>
          <form action={runWorkloadAnalysis}>
            <button type="submit" style={heroButtonStyle}>Run Workload Analysis</button>
          </form>
        </section>

        <section style={{ display: 'grid', gridTemplateColumns: 'repeat(6,minmax(0,1fr))', gap: 12 }}>
          <Kpi title="Users Analyzed" value={snapshots.length} sub="latest snapshots" tone="#2563eb" />
          <Kpi title="Critical" value={critical.length} sub="overloaded" tone="#dc2626" />
          <Kpi title="High" value={high.length} sub="pressure" tone="#d97706" />
          <Kpi title="Available" value={low.length} sub="low load" tone="#16a34a" />
          <Kpi title="Recommendations" value={recs.length} sub="pending approval" tone="#7c3aed" />
          <Kpi title="Avg Pressure" value={avgPressure} sub="weighted load" tone="#0f172a" />
        </section>

        <section style={{ display: 'grid', gridTemplateColumns: '1.2fr .8fr', gap: 18, alignItems: 'start' }}>
          <Panel title="Latest Workload Map" subtitle="Agent-level pressure generated by tasks, overdue work, prospects, gaps, and value.">
            <div style={{ display: 'grid', gridTemplateColumns: 'repeat(3,minmax(0,1fr))', gap: 12 }}>
              {snapshots.length ? snapshots.map((snapshot: any) => (
                <LoadCard key={snapshot.id} snapshot={snapshot} userName={userName(snapshot.user_id)} />
              )) : <EmptyState title="No snapshots yet" text="Run workload analysis to create the first workload map." />}
            </div>
          </Panel>

          <Panel title="Create Manager Intervention" subtitle="Push an action to a target user or team situation.">
            <form action={createManagerIntervention} style={formStyle}>
              <input name="title" required placeholder="Intervention title" style={inputStyle} />
              <textarea name="description" rows={4} placeholder="Manager instruction / required correction..." style={inputStyle} />
              <select name="severity" defaultValue="medium" style={inputStyle}>
                <option value="low">Low</option>
                <option value="medium">Medium</option>
                <option value="warning">Warning</option>
                <option value="critical">Critical</option>
              </select>
              <select name="target_user_id" style={inputStyle}>
                <option value="">No specific user</option>
                {allUsers.map((u: any) => <option key={u.id} value={u.id}>{u.full_name || u.username} — {u.role}</option>)}
              </select>
              <input name="due_at" type="datetime-local" style={inputStyle} />
              <button type="submit" style={buttonStyle}>Create Intervention</button>
            </form>
          </Panel>
        </section>

        <Panel title="Rebalance Recommendations" subtitle="Approve or reject system recommendations. Approval updates task assignment or prospect ownership.">
          <div style={{ display: 'grid', gridTemplateColumns: 'repeat(2,minmax(0,1fr))', gap: 12 }}>
            {recs.length ? recs.map((rec: any) => (
              <article key={rec.id} style={recCardStyle}>
                <div style={{ display: 'flex', justifyContent: 'space-between', gap: 10 }}>
                  <Badge tone={statusTone(rec.severity)}>{String(rec.severity || 'medium').toUpperCase()}</Badge>
                  <Badge tone="#64748b">{rec.related_type}</Badge>
                </div>
                <h3 style={cardTitleStyle}>{rec.recommendation_type}</h3>
                <p style={cardTextStyle}>{rec.reason}</p>
                <p style={cardTextStyle}>From: <strong>{userName(rec.from_user_id)}</strong> → To: <strong>{userName(rec.to_user_id)}</strong></p>
                <div style={{ display: 'flex', gap: 8, flexWrap: 'wrap' }}>
                  <form action={approveRebalance}>
                    <input type="hidden" name="id" value={rec.id} />
                    <button type="submit" style={buttonStyle}>Approve</button>
                  </form>
                  <form action={rejectRebalance}>
                    <input type="hidden" name="id" value={rec.id} />
                    <button type="submit" style={lightButtonAsButtonStyle}>Reject</button>
                  </form>
                </div>
              </article>
            )) : <EmptyState title="No pending recommendations" text="Run workload analysis to generate reassignment recommendations." />}
          </div>
        </Panel>

        <section style={{ display: 'grid', gridTemplateColumns: '1fr .75fr', gap: 18, alignItems: 'start' }}>
          <Panel title="Open Manager Interventions" subtitle="Manual pressure actions created by managers.">
            <div style={{ display: 'grid', gap: 12 }}>
              {interventions.length ? interventions.map((item: any) => (
                <article key={item.id} style={recCardStyle}>
                  <div style={{ display: 'flex', justifyContent: 'space-between', gap: 10 }}>
                    <Badge tone={statusTone(item.severity)}>{String(item.severity || 'medium').toUpperCase()}</Badge>
                    <span style={{ color: '#64748b', fontWeight: 800 }}>{formatDate(item.due_at)}</span>
                  </div>
                  <h3 style={cardTitleStyle}>{item.title}</h3>
                  <p style={cardTextStyle}>{item.description || 'No description.'}</p>
                  <p style={cardTextStyle}>Target: <strong>{userName(item.target_user_id)}</strong></p>
                  <form action={closeManagerIntervention}>
                    <input type="hidden" name="id" value={item.id} />
                    <button type="submit" style={buttonStyle}>Close</button>
                  </form>
                </article>
              )) : <EmptyState title="No interventions" text="No open manager interventions." />}
            </div>
          </Panel>

          <Panel title="Manager Shortcuts" subtitle="Use these surfaces after rebalancing.">
            <div style={{ display: 'flex', gap: 10, flexWrap: 'wrap' }}>
              <ActionLink href="/revenue-command-center/tasks/board">Task Board</ActionLink>
              <ActionLink href="/revenue-command-center/prospects/pipeline" variant="light">Pipeline Board</ActionLink>
              <ActionLink href="/revenue-command-center/control-tower" variant="light">Control Tower</ActionLink>
              <ActionLink href="/revenue-command-center/system-activation" variant="light">System Activation</ActionLink>
              <ActionLink href="/revenue-command-center/ai-scoring" variant="light">AI Scoring</ActionLink>
            </div>
          </Panel>
        </section>
      </div>
    </AppShell>
  )
}

const heroStyle: React.CSSProperties = { display: 'flex', justifyContent: 'space-between', alignItems: 'center', gap: 18, padding: 30, borderRadius: 32, color: '#fff', background: 'radial-gradient(circle at top left,#2563eb,#020617 68%)', boxShadow: '0 28px 70px rgba(15,23,42,.22)' }
const eyebrowStyle: React.CSSProperties = { display: 'inline-flex', padding: '7px 12px', borderRadius: 999, background: 'rgba(255,255,255,.12)', color: '#dbeafe', fontWeight: 950, fontSize: 12, marginBottom: 12 }
const heroTitleStyle: React.CSSProperties = { margin: 0, fontSize: 38, fontWeight: 950 }
const heroTextStyle: React.CSSProperties = { margin: '10px 0 0', color: '#dbeafe', fontWeight: 750, maxWidth: 820, lineHeight: 1.6 }
const heroButtonStyle: React.CSSProperties = { border: '1px solid rgba(255,255,255,.24)', borderRadius: 16, padding: '14px 18px', background: 'rgba(255,255,255,.12)', color: '#fff', fontWeight: 950, cursor: 'pointer' }
const formStyle: React.CSSProperties = { display: 'grid', gap: 10 }
const inputStyle: React.CSSProperties = { width: '100%', boxSizing: 'border-box', padding: 13, borderRadius: 13, border: '1px solid #cbd5e1', background: '#f8fafc', color: '#0f172a', fontWeight: 750 }
const buttonStyle: React.CSSProperties = { border: 'none', borderRadius: 14, padding: '12px 14px', background: '#0f172a', color: '#fff', fontWeight: 950, cursor: 'pointer' }
const lightButtonAsButtonStyle: React.CSSProperties = { border: '1px solid #dbe3ee', borderRadius: 14, padding: '12px 14px', background: '#fff', color: '#0f172a', fontWeight: 950, cursor: 'pointer' }
const recCardStyle: React.CSSProperties = { padding: 16, borderRadius: 20, background: 'linear-gradient(180deg,#fff,#f8fafc)', border: '1px solid #e2e8f0', display: 'grid', gap: 10, color: '#0f172a' }
const cardTitleStyle: React.CSSProperties = { margin: 0, fontSize: 17, fontWeight: 950, color: '#0f172a' }
const cardTextStyle: React.CSSProperties = { margin: 0, color: '#64748b', lineHeight: 1.55, fontWeight: 700 }
