import { createClient } from '@/lib/supabase/server'
import { requireAccess } from '@/lib/auth/requireAccess'
import { createDocumentExport } from '../../_actionsDocuments'
import { ExportReasonForm, PrintReadyCorporateDocument } from '../../_components/DocumentExportCenter'
import { getDocumentTemplate } from '../../_lib/documentCatalog'
import { getConfidentialityLabel, getPurposeDescription } from '../../_lib/documentGovernance'

export default async function AcademyDocumentExportPage({
  searchParams,
}: {
  searchParams?: Promise<{ type?: string; exportId?: string }>
}) {
  await requireAccess('academy.view')
  const params = await searchParams
  const supabase = await createClient()

  if (params?.exportId) {
    const { data: exportRow, error } = await supabase
      .from('academy_document_exports')
      .select('*')
      .eq('id', params.exportId)
      .maybeSingle()

    if (error) throw new Error(error.message)
    const template = getDocumentTemplate(exportRow?.document_type)

    const [{ data: trainees }, { data: trainers }, { data: groups }, { data: courses }, { data: payments }, { data: attendance }, { data: certificates }, { data: partners }] = await Promise.all([
      supabase.from('academy_trainees').select('id,serial_number,full_name,city,status,eligibility_status,created_at').limit(40),
      supabase.from('academy_trainers').select('id,full_name,specialty,status,created_at').limit(30),
      supabase.from('academy_groups').select('id,name,status,location,start_date,end_date,max_capacity').limit(30),
      supabase.from('academy_courses').select('id,title,category,level,duration_hours,price,status').limit(30),
      supabase.from('academy_payments').select('id,trainee_id,amount,status,method,created_at').limit(80),
      supabase.from('academy_attendance').select('id,trainee_id,group_id,status,session_date').limit(120),
      supabase.from('academy_certificates').select('id,trainee_id,course_id,certificate_number,serial_number,status,issued_at').limit(50),
      supabase.from('academy_partners').select('id,name,type,city,status').limit(40),
    ])

    const paid = (payments || []).filter((p: any) => p.status === 'paid').reduce((s: number, p: any) => s + Number(p.amount || 0), 0)
    const open = (payments || []).filter((p: any) => p.status !== 'paid').reduce((s: number, p: any) => s + Number(p.amount || 0), 0)

    return (
      <PrintReadyCorporateDocument
        title={exportRow.document_title || template?.title || 'Academy Document'}
        subtitle={template?.description || 'Controlled Academy export generated by OpsOS.'}
        reference={exportRow.document_reference}
        meta={[
          ['Generated by', exportRow.generated_by_name || 'AngelCare User'],
          ['Generated at', exportRow.created_at ? new Date(exportRow.created_at).toLocaleString('fr-FR') : '—'],
          ['Purpose', exportRow.export_reason || '—'],
          ['Confidentiality', getConfidentialityLabel(exportRow.confidentiality)],
          ['Category', exportRow.document_category || '—'],
          ['Code', exportRow.document_code || '—'],
          ['Status', exportRow.status || 'generated'],
          ['Filters', summarizeFilters(exportRow.filters)],
        ]}
      >
        <section style={section}>
          <h2>Governance Purpose</h2>
          <p>{getPurposeDescription(exportRow.export_reason || '')}</p>
          {exportRow.export_reason_note && <p><strong>Manager note:</strong> {exportRow.export_reason_note}</p>}
        </section>

        <section style={kpiGrid}>
          <Kpi label="Trainees" value={String(trainees?.length || 0)} />
          <Kpi label="Trainers" value={String(trainers?.length || 0)} />
          <Kpi label="Groups" value={String(groups?.length || 0)} />
          <Kpi label="Courses" value={String(courses?.length || 0)} />
          <Kpi label="Collected" value={`${paid.toLocaleString('fr-FR')} MAD`} />
          <Kpi label="Outstanding" value={`${open.toLocaleString('fr-FR')} MAD`} />
          <Kpi label="Attendance logs" value={String(attendance?.length || 0)} />
          <Kpi label="Certificates" value={String(certificates?.length || 0)} />
        </section>

        <section style={section}>
          <h2>Executive Reading</h2>
          <p>This controlled document consolidates Academy data for management review, governance evidence, decision-making and operational traceability. Values reflect the current OpsOS database snapshot at generation time.</p>
        </section>

        <section style={section}>
          <h2>Controlled Record Sample</h2>
          <table style={table}>
            <thead><tr><th>Serial</th><th>Name</th><th>City</th><th>Status</th><th>Eligibility</th></tr></thead>
            <tbody>{(trainees || []).slice(0, 24).map((t: any) => <tr key={t.id}><td>{t.serial_number || '—'}</td><td>{t.full_name || '—'}</td><td>{t.city || '—'}</td><td>{t.status || '—'}</td><td>{t.eligibility_status || '—'}</td></tr>)}</tbody>
          </table>
        </section>

        <section style={section}>
          <h2>Required Management Controls</h2>
          <ul>
            <li>Verify that the selected export purpose matches the actual business use.</li>
            <li>Confirm that confidential or board-level documents are only shared with authorized stakeholders.</li>
            <li>Archive the generated PDF with its reference number and retain the export log in OpsOS.</li>
            <li>Use the reference number in any related email, partner communication or audit file.</li>
          </ul>
        </section>
      </PrintReadyCorporateDocument>
    )
  }

  const template = getDocumentTemplate(params?.type)

  if (!template) {
    return <main style={{ padding: 30 }}><h1>Document template not found</h1><p>Return to /academy/documents and select a valid template.</p></main>
  }

  return (
    <main style={{ padding: 24, display: 'grid', gap: 18 }}>
      <section style={intro}>
        <span style={badge}>{template.code}</span>
        <h1>{template.title}</h1>
        <p>{template.description}</p>
      </section>
      <form action={createDocumentExport}>
        <ExportReasonForm type={template.type} defaultReason={template.recommendedPurpose} />
      </form>
    </main>
  )
}

function summarizeFilters(filters: any) {
  if (!filters) return 'All data'
  const active = Object.entries(filters).filter(([, v]) => Boolean(v)).map(([k, v]) => `${k}: ${v}`)
  return active.length ? active.join(' · ') : 'All data'
}
function Kpi({ label, value }: { label: string; value: string }) { return <div style={kpi}><span>{label}</span><strong>{value}</strong></div> }
const intro: React.CSSProperties = { padding: 26, borderRadius: 28, background: '#0f172a', color: '#fff' }
const badge: React.CSSProperties = { display: 'inline-flex', padding: '7px 10px', borderRadius: 999, background: '#dbeafe', color: '#1e3a8a', fontWeight: 950 }
const section: React.CSSProperties = { padding: 18, borderRadius: 18, border: '1px solid #dbe3ee', background: '#fff' }
const kpiGrid: React.CSSProperties = { display: 'grid', gridTemplateColumns: 'repeat(4,minmax(0,1fr))', gap: 12 }
const kpi: React.CSSProperties = { padding: 16, borderRadius: 18, border: '1px solid #dbe3ee', background: '#fff', display: 'grid', gap: 6 }
const table: React.CSSProperties = { width: '100%', borderCollapse: 'collapse' }
