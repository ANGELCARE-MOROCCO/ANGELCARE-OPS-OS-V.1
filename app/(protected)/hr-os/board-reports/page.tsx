import HrOsShell from '@/app/components/hr-os/HrOsShell'
import { ActionButton, Badge, Kpi, Panel, inputStyle } from '@/app/components/hr-os/EliteCards'
import { createClient } from '@/lib/supabase/server'
import { requireAccess } from '@/lib/auth/requireAccess'
import { HR_BOARD_REPORTS } from '../_lib/hrBoardReporting'
import { createHrBoardExport } from '../_actionsBoardReports'

export default async function HrBoardReportsPage() {
  await requireAccess('hr.view')
  const supabase = await createClient()

  let exports: any[] = []
  let actions: any[] = []
  try {
    const [{ data: e }, { data: a }] = await Promise.all([
      supabase.from('hr_os_board_exports').select('*').order('created_at', { ascending: false }).limit(40),
      supabase.from('hr_os_actions').select('*').order('created_at', { ascending: false }).limit(100),
    ])
    exports = e || []
    actions = a || []
  } catch {
    exports = []
    actions = []
  }

  const openActions = actions.filter((a) => a.status !== 'closed').length
  const highPriority = actions.filter((a) => a.priority === 'high').length
  const complianceSignals = actions.filter((a) => `${a.title} ${a.notes}`.toLowerCase().includes('document') || `${a.title} ${a.notes}`.toLowerCase().includes('compliance')).length

  return (
    <HrOsShell
      title="HR-OS V10 Board Reporting"
      subtitle="Board-ready reporting and controlled export governance for HR operations, workforce risk, compliance and executive review."
      active="board-reports"
    >
      <div style={{ display: 'grid', gap: 18 }}>
        <div style={{ display: 'grid', gridTemplateColumns: 'repeat(4,1fr)', gap: 12 }}>
          <Kpi label="Available Reports" value={HR_BOARD_REPORTS.length} tone="#2563eb" />
          <Kpi label="Exports Logged" value={exports.length} tone="#7c3aed" />
          <Kpi label="Open HR Actions" value={openActions} tone="#f59e0b" />
          <Kpi label="Compliance Signals" value={complianceSignals} tone="#ef4444" />
        </div>

        <div style={{ display: 'grid', gridTemplateColumns: '440px 1fr', gap: 18, alignItems: 'start' }}>
          <Panel title="Generate Controlled Export" subtitle="Every export requires purpose/reason and creates a governed reference number." tone="#7c3aed">
            <form action={createHrBoardExport} style={{ display: 'grid', gap: 10 }}>
              <select name="type" style={inputStyle}>
                {HR_BOARD_REPORTS.map((report) => <option key={report.type} value={report.type}>{report.title}</option>)}
              </select>
              <textarea name="reason" required placeholder="Required export reason: board review, audit, management meeting, compliance evidence..." style={{ ...inputStyle, minHeight: 90 }} />
              <input name="generated_by" placeholder="Generated by" style={inputStyle} />
              <div style={{ display: 'grid', gridTemplateColumns: '1fr 1fr', gap: 10 }}>
                <input name="period_start" type="datetime-local" style={inputStyle} />
                <input name="period_end" type="datetime-local" style={inputStyle} />
              </div>
              <div style={{ display: 'grid', gridTemplateColumns: '1fr 1fr 1fr', gap: 10 }}>
                <input name="city" placeholder="City filter" style={inputStyle} />
                <input name="department" placeholder="Department" style={inputStyle} />
                <select name="confidentiality" style={inputStyle}>
                  <option value="internal">Internal</option>
                  <option value="restricted">Restricted</option>
                  <option value="board">Board</option>
                </select>
              </div>
              <ActionButton>Generate Export Record</ActionButton>
            </form>
          </Panel>

          <Panel title="Report Catalog" subtitle="Corporate HR templates ready for governance-controlled export." tone="#0f172a">
            <div style={{ display: 'grid', gridTemplateColumns: 'repeat(2,minmax(0,1fr))', gap: 12 }}>
              {HR_BOARD_REPORTS.map((report) => (
                <div key={report.type} style={card}>
                  <Badge tone="#2563eb">{report.type}</Badge>
                  <h3 style={{ margin: '9px 0 5px' }}>{report.title}</h3>
                  <p style={{ margin: 0, color: '#64748b', fontWeight: 750, lineHeight: 1.5 }}>{report.description}</p>
                </div>
              ))}
            </div>
          </Panel>
        </div>

        <Panel title="Export Governance Log" subtitle="Traceable export records with reference numbers, purpose, filters and author." tone="#16a34a">
          <div style={{ display: 'grid', gap: 10 }}>
            {exports.length ? exports.map((item) => (
              <div key={item.id} style={row}>
                <div>
                  <strong>{item.reference}</strong>
                  <p style={{ margin: '5px 0 0', color: '#64748b', fontWeight: 750 }}>{item.reason || 'No reason captured'}</p>
                  <small>{item.type} · {item.generated_by || '—'} · {item.created_at ? new Date(item.created_at).toLocaleString('fr-FR') : '—'}</small>
                </div>
                <Badge tone="#7c3aed">{item.filters?.confidentiality || 'internal'}</Badge>
              </div>
            )) : <p style={{ color: '#64748b', fontWeight: 850 }}>No governed exports yet.</p>}
          </div>
        </Panel>
      </div>
    </HrOsShell>
  )
}

const card: React.CSSProperties = { padding: 16, borderRadius: 18, background: '#f8fafc', border: '1px solid #dbe3ee' }
const row: React.CSSProperties = { display: 'flex', justifyContent: 'space-between', gap: 16, alignItems: 'center', padding: 15, borderRadius: 18, background: '#fff', border: '1px solid #e2e8f0' }
