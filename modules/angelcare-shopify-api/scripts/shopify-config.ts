#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';

import {
  envPath,
  getPublicAppUrl,
  loadEnv,
  validateConfig,
} from '../src/config/env';
import type { AppEnv } from '../src/types';

function splitList(value: unknown) {
  return String(value || '')
    .split(',')
    .map((item) => item.trim())
    .filter(Boolean);
}

function tomlString(value: unknown) {
  return JSON.stringify(String(value));
}

function tomlArray(values: string[]) {
  return `[${values.map((value) => tomlString(value)).join(', ')}]`;
}

function envBool(value: unknown) {
  return String(value || '').toLowerCase() === 'true';
}

function parseArgs(argv: string[]) {
  const args: { output?: string } = {};

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === '--output') {
      args.output = argv[index + 1];
      index += 1;
    }
  }

  return args;
}

function buildShopifyAppToml(env: AppEnv) {
  validateConfig(env, [
    'SHOPIFY_APP_NAME',
    'SHOPIFY_CLIENT_ID',
    'SHOPIFY_SHOP_DOMAIN',
    'SHOPIFY_API_VERSION',
    'SHOPIFY_SCOPES',
  ]);

  const appUrl = getPublicAppUrl(env);
  const callbackUrl = `${appUrl}/api/auth/callback`;
  const webhookUri = env.SHOPIFY_WEBHOOK_URI || env.SHOPIFY_WEBHOOK_PATH;
  const webhookTopics = splitList(env.SHOPIFY_WEBHOOK_TOPICS);
  const complianceTopics = splitList(env.SHOPIFY_WEBHOOK_COMPLIANCE_TOPICS);
  const legacyInstallFlow = env.SHOPIFY_OMIT_AUTH_SCOPES !== 'true';

  const lines = [
    '# Generated by npm run shopify:config. Do not put SHOPIFY_CLIENT_SECRET in this file.',
    `name = ${tomlString(env.SHOPIFY_APP_NAME)}`,
    env.SHOPIFY_APP_HANDLE ? `handle = ${tomlString(env.SHOPIFY_APP_HANDLE)}` : '',
    `client_id = ${tomlString(env.SHOPIFY_CLIENT_ID)}`,
    `application_url = ${tomlString(appUrl)}`,
    `embedded = ${envBool(env.SHOPIFY_EMBEDDED) ? 'true' : 'false'}`,
    'web_directories = ["."]',
    '',
    '[access_scopes]',
    `scopes = ${tomlString(env.SHOPIFY_SCOPES)}`,
    `use_legacy_install_flow = ${legacyInstallFlow ? 'true' : 'false'}`,
    '',
    '[auth]',
    `redirect_urls = ${tomlArray([callbackUrl])}`,
    '',
    '[webhooks]',
    `api_version = ${tomlString(env.SHOPIFY_API_VERSION)}`,
  ];

  if (webhookTopics.length || complianceTopics.length) {
    lines.push('', '[[webhooks.subscriptions]]');
    if (webhookTopics.length) {
      lines.push(`topics = ${tomlArray(webhookTopics)}`);
    }
    if (complianceTopics.length) {
      lines.push(`compliance_topics = ${tomlArray(complianceTopics)}`);
    }
    lines.push(`uri = ${tomlString(webhookUri)}`);
  }

  lines.push(
    '',
    '[app_preferences]',
    `url = ${tomlString(`${appUrl}/dashboard`)}`,
    '',
    '[build]',
    `automatically_update_urls_on_dev = ${envBool(env.SHOPIFY_AUTOMATICALLY_UPDATE_URLS_ON_DEV) ? 'true' : 'false'}`,
    '',
  );

  return lines.filter((line) => line !== '').join('\n');
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const env = loadEnv(envPath);
  const outputPath = path.resolve(process.cwd(), args.output || env.SHOPIFY_APP_CONFIG || 'shopify.app.toml');
  const contents = buildShopifyAppToml(env);

  fs.writeFileSync(outputPath, `${contents}\n`, { mode: 0o600 });
  console.log(`Wrote ${path.relative(process.cwd(), outputPath)}`);
  console.log('Next: run `shopify app deploy` to release the app configuration and webhooks.');
}

try {
  main();
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
}
