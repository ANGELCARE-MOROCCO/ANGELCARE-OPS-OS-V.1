create or replace function public.market_os_ambassador_guard_settings_approval_mutation()
returns trigger
language plpgsql
as $$
begin
  if old.status <> 'pending' then
    raise exception 'Decided Ambassador settings approvals are immutable' using errcode = '42501';
  end if;
  if new.status not in ('approved','rejected','revision_requested') then
    raise exception 'Invalid Ambassador settings approval decision' using errcode = '23514';
  end if;
  if new.decided_by_actor_id is null or new.decided_at is null then
    raise exception 'Approval decision requires an actor and timestamp' using errcode = '23514';
  end if;
  return new;
end $$;

