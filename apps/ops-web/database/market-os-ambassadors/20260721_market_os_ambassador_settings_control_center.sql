create or replace function public.market_os_ambassador_submit_settings_version(
  p_version_id uuid,
  p_actor_id uuid,
  p_request_id text,
  p_validation_result jsonb,
  p_required_approvals text[]
) returns jsonb
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  target public.market_os_ambassador_settings_versions%rowtype;
  next_round integer;
  actor_role text;
  actor_name text;
  existing_result jsonb;
  v_idempotency_key text;
  audit_tenant public.market_os_ambassador_audit_logs.tenant_id%type;
  audit_organization public.market_os_ambassador_audit_logs.organization_id%type;
begin
  select * into target
  from public.market_os_ambassador_settings_versions
  where id = p_version_id
  for update;

  if target.id is null then
    raise exception 'Settings version not found' using errcode = 'P0002';
  end if;
  if not public.market_os_ambassador_actor_has_permission(
    p_actor_id, 'settings.submit', target.tenant_id, target.organization_id
  ) then
    raise exception 'Actor lacks settings.submit permission' using errcode = '42501';
  end if;
  if nullif(btrim(coalesce(p_request_id, '')), '') is null then
    raise exception 'Settings submission requires an idempotency request id' using errcode = '23514';
  end if;
  v_idempotency_key := target.id::text || ':' || p_request_id;
  select result into existing_result
  from public.market_os_ambassador_idempotency
  where tenant_id = target.tenant_id and organization_id = target.organization_id
    and operation = 'settings_submit' and idempotency_key = v_idempotency_key
    and actor_id = p_actor_id;
  if existing_result is not null then
    return existing_result || jsonb_build_object('idempotent', true);
  end if;
  if target.status not in ('draft','revision_requested') then
    raise exception 'Only editable settings drafts can be submitted' using errcode = '23514';
  end if;
  if coalesce((p_validation_result ->> 'valid')::boolean, false) is not true then
    raise exception 'Settings validation must pass before submission' using errcode = '23514';
  end if;
  if coalesce(cardinality(p_required_approvals), 0) = 0 then
    raise exception 'At least one approval domain is required' using errcode = '23514';
  end if;
  if exists (
    select 1 from unnest(p_required_approvals) domain
    where domain not in ('program','compliance','finance','system')
  ) then
    raise exception 'Unsupported settings approval domain' using errcode = '23514';
  end if;

  select role_key, coalesce(display_name, auth_user_id::text)
    into actor_role, actor_name
  from public.market_os_ambassador_actor_roles
  where id = p_actor_id and status = 'active';

  next_round := coalesce(target.approval_round, 0) + 1;
  audit_tenant := target.tenant_id;
  audit_organization := target.organization_id;

  update public.market_os_ambassador_settings_versions
  set status = 'pending_approval',
      approval_round = next_round,
      validation_result = p_validation_result,
      submitted_at = now(),
      submitted_by_actor_id = p_actor_id,
      updated_by_actor_id = p_actor_id,
      updated_at = now()
  where id = target.id
  returning * into target;

  insert into public.market_os_ambassador_settings_approvals (
    id, tenant_id, organization_id, version_id, approval_round,
    approval_domain, status, created_at, updated_at
  )
  select gen_random_uuid(), target.tenant_id, target.organization_id, target.id,
         next_round, domain, 'pending', now(), now()
  from (select distinct unnest(p_required_approvals) as domain) required;

  insert into public.market_os_ambassador_settings_runtime_events (
    id, tenant_id, organization_id, version_id, event_type, status,
    summary, details, actor_id, request_id, created_at
  ) values (
    gen_random_uuid(), target.tenant_id, target.organization_id, target.id,
    'approval_submission', 'pending',
    'Settings version submitted for controlled approval',
    jsonb_build_object('requiredApprovals', p_required_approvals, 'approvalRound', next_round),
    p_actor_id, p_request_id, now()
  );

  insert into public.market_os_ambassador_audit_logs (
    id, tenant_id, organization_id, entity_type, entity_id, action, summary,
    actor_id, actor_name, actor_role, request_id, payload,
    before_snapshot, after_snapshot, created_at
  ) values (
    gen_random_uuid(), audit_tenant, audit_organization,
    'market_os_ambassador_settings_versions', target.id,
    'settings_submitted_for_approval_transactionally',
    'Submitted Ambassador settings version for controlled approval',
    p_actor_id, actor_name, actor_role, p_request_id,
    jsonb_build_object('requiredApprovals', p_required_approvals, 'approvalRound', next_round),
    null, to_jsonb(target), now()
  );

  existing_result := to_jsonb(target);
  insert into public.market_os_ambassador_idempotency (
    tenant_id, organization_id, operation, idempotency_key, actor_id, result
  ) values (
    target.tenant_id, target.organization_id, 'settings_submit', v_idempotency_key, p_actor_id, existing_result
  ) on conflict (tenant_id, organization_id, operation, idempotency_key)
    do update set result = excluded.result;

  return existing_result;
end $$;

create or replace function public.market_os_ambassador_decide_settings_approval(
  p_version_id uuid,
  p_actor_id uuid,
  p_domain text,
  p_decision text,
  p_note text,
  p_request_id text
) returns jsonb
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  target public.market_os_ambassador_settings_versions%rowtype;
  approval_record public.market_os_ambassador_settings_approvals%rowtype;
  actor_role text;
  actor_name text;
  next_status text;
  remaining integer;
  domain_authorized boolean := false;
  existing_result jsonb;
  v_idempotency_key text;
  audit_tenant public.market_os_ambassador_audit_logs.tenant_id%type;
  audit_organization public.market_os_ambassador_audit_logs.organization_id%type;
begin
  select * into target
  from public.market_os_ambassador_settings_versions
  where id = p_version_id
  for update;

  if target.id is null then
    raise exception 'Settings version not found' using errcode = 'P0002';
  end if;
  if not public.market_os_ambassador_actor_has_permission(
    p_actor_id, 'settings.approve', target.tenant_id, target.organization_id
  ) then
    raise exception 'Actor lacks settings.approve permission' using errcode = '42501';
  end if;
  if nullif(btrim(coalesce(p_request_id, '')), '') is null then
    raise exception 'Settings approval requires an idempotency request id' using errcode = '23514';
  end if;
  v_idempotency_key := target.id::text || ':' || coalesce(p_domain, '') || ':' || coalesce(p_decision, '') || ':' || p_request_id;
  select result into existing_result
  from public.market_os_ambassador_idempotency
  where tenant_id = target.tenant_id and organization_id = target.organization_id
    and operation = 'settings_approval' and idempotency_key = v_idempotency_key
    and actor_id = p_actor_id;
  if existing_result is not null then
    return existing_result || jsonb_build_object('idempotent', true);
  end if;
  if target.status <> 'pending_approval' then
    raise exception 'This settings version is not awaiting approval' using errcode = '23514';
  end if;
  if p_domain not in ('program','compliance','finance','system') then
    raise exception 'Invalid approval domain' using errcode = '23514';
  end if;
  if p_decision not in ('approved','rejected','revision_requested') then
    raise exception 'Invalid approval decision' using errcode = '23514';
  end if;
  if nullif(btrim(coalesce(p_note, '')), '') is null then
    raise exception 'Approval decision note is required' using errcode = '23514';
  end if;
  select role_key, coalesce(display_name, auth_user_id::text)
    into actor_role, actor_name
  from public.market_os_ambassador_actor_roles
  where id = p_actor_id and status = 'active'
    and tenant_id = target.tenant_id and organization_id = target.organization_id;

  domain_authorized := actor_role = 'ambassador_admin'
    or public.market_os_ambassador_actor_has_permission(
      p_actor_id, '*', target.tenant_id, target.organization_id
    )
    or (actor_role = 'market_manager' and p_domain = 'program')
    or (actor_role = 'compliance' and p_domain = 'compliance')
    or (actor_role = 'finance' and p_domain = 'finance');

  if not coalesce(domain_authorized, false) then
    raise exception 'Actor role cannot decide % settings approval', p_domain using errcode = '42501';
  end if;
  if coalesce((target.configuration #>> '{governance,separationOfDutiesRequired}')::boolean, true)
    and p_domain in ('finance','compliance')
    and p_actor_id in (target.created_by_actor_id, target.submitted_by_actor_id) then
    raise exception '% approval must be decided by an actor other than the draft author or submitter', p_domain
      using errcode = '42501';
  end if;

  select * into approval_record
  from public.market_os_ambassador_settings_approvals
  where tenant_id = target.tenant_id
    and organization_id = target.organization_id
    and version_id = target.id
    and approval_round = target.approval_round
    and approval_domain = p_domain
    and status = 'pending'
  for update;

  if approval_record.id is null then
    raise exception 'No pending % approval exists', p_domain using errcode = 'P0002';
  end if;

  update public.market_os_ambassador_settings_approvals
  set status = p_decision,
      decision_note = btrim(p_note),
      decided_by_actor_id = p_actor_id,
      decided_at = now(),
      updated_at = now()
  where id = approval_record.id
  returning * into approval_record;

  next_status := 'pending_approval';
  if p_decision = 'rejected' then
    next_status := 'rejected';
  elsif p_decision = 'revision_requested' then
    next_status := 'revision_requested';
  else
    select count(*) into remaining
    from public.market_os_ambassador_settings_approvals
    where version_id = target.id
      and approval_round = target.approval_round
      and status = 'pending';
    if remaining = 0 then next_status := 'approved'; end if;
  end if;

