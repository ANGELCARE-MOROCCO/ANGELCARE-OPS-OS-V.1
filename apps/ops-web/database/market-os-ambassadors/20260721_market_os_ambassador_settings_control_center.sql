create or replace function public.market_os_ambassador_effective_settings_configuration(
  p_tenant_id text,
  p_organization_id text,
  p_scope_type text default null,
  p_scope_key text default null
) returns jsonb
language sql
stable
security definer
set search_path = public, pg_temp
as $$
  select coalesce((
    select version.configuration
    from public.market_os_ambassador_settings_active_scopes active
    join public.market_os_ambassador_settings_versions version
      on version.id = active.current_version_id
    where active.tenant_id = p_tenant_id
      and active.organization_id = p_organization_id
      and version.status = 'published'
      and (
        (active.scope_type = p_scope_type and lower(active.scope_key) = lower(coalesce(p_scope_key, '')))
        or (active.scope_type = 'organization' and active.scope_key = 'default')
      )
    order by case
      when active.scope_type = p_scope_type and lower(active.scope_key) = lower(coalesce(p_scope_key, '')) then 0
      else 1
    end
    limit 1
  ), '{}'::jsonb)
$$;

create or replace function public.market_os_ambassador_enforce_territory_policy()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  configuration jsonb;
  territory_rules jsonb;
  maximum_active integer;
  approved_count integer;
  allow_backup boolean;
  allow_temporary boolean;
  maximum_radius numeric;
begin
  configuration := public.market_os_ambassador_effective_settings_configuration(
    new.tenant_id, new.organization_id, 'territory', new.territory_id::text
  );
  territory_rules := coalesce(configuration -> 'territories', '{}'::jsonb);
  maximum_active := coalesce((territory_rules ->> 'maximumAmbassadorsPerTerritory')::integer, 25);
  allow_backup := coalesce((territory_rules ->> 'allowBackupAssignments')::boolean, true);
  allow_temporary := coalesce((territory_rules ->> 'allowTemporaryAssignments')::boolean, true);
  maximum_radius := coalesce((territory_rules ->> 'travelRadiusKm')::numeric, 50);

  if new.assignment_type = 'backup' and not allow_backup then
    raise exception 'Backup territory assignments are disabled by the effective Ambassador policy' using errcode = '23514';
  end if;
  if new.assignment_type = 'temporary' and not allow_temporary then
    raise exception 'Temporary territory assignments are disabled by the effective Ambassador policy' using errcode = '23514';
  end if;
  if new.radius_km is not null and new.radius_km > maximum_radius then
    raise exception 'Territory assignment radius exceeds the effective policy limit of % km', maximum_radius using errcode = '23514';
  end if;

  if new.status = 'approved' then
    if tg_op = 'UPDATE' and old.status = 'approved' then
      return new;
    end if;
    perform pg_advisory_xact_lock(hashtext(new.tenant_id || ':' || new.organization_id || ':territory:' || new.territory_id::text)::bigint);
    select count(*) into approved_count
    from public.market_os_ambassador_territory_assignments assignment
    where assignment.tenant_id = new.tenant_id
      and assignment.organization_id = new.organization_id
      and assignment.territory_id = new.territory_id
      and assignment.status = 'approved'
      and assignment.archived_at is null
      and assignment.id <> new.id;
    if approved_count >= maximum_active then
      raise exception 'Territory capacity is limited to % active Ambassadors by the effective policy', maximum_active using errcode = '23514';
    end if;
  end if;
  return new;
end $$;

