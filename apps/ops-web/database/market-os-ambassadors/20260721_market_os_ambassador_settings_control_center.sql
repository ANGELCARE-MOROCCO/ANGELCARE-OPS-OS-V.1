create or replace function public.market_os_ambassador_guard_settings_version_lifecycle()
returns trigger
language plpgsql
as $$
declare
  allowed text[];
begin
  if old.status = new.status then
    return new;
  end if;

  allowed := case old.status
    when 'draft' then array['pending_approval','cancelled']
    when 'revision_requested' then array['draft','pending_approval','cancelled']
    when 'pending_approval' then array['approved','rejected','revision_requested','cancelled']
    when 'approved' then array['scheduled','published','cancelled']
    when 'scheduled' then array['published','approved','cancelled']
    when 'published' then array['superseded','rolled_back']
    when 'superseded' then array['rolled_back']
    else array[]::text[]
  end;

  if not (new.status = any(allowed)) then
    raise exception 'Invalid Ambassador settings version transition: % -> %', old.status, new.status
      using errcode = '23514';
  end if;
  return new;
end $$;

