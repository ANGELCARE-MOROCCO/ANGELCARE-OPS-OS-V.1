create or replace function public.market_os_ambassador_enforce_mission_assignment_policy()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  configuration jsonb;
  mission_rules jsonb;
  maximum_concurrent integer;
  active_count integer;
  mission_territory text;
begin
  select mission.territory_id::text into mission_territory
  from public.market_os_ambassador_missions mission
  where mission.id = new.mission_id;

  configuration := public.market_os_ambassador_effective_settings_configuration(
    new.tenant_id, new.organization_id,
    case when mission_territory is null then 'organization' else 'territory' end,
    case when mission_territory is null then 'default' else mission_territory end
  );
  mission_rules := coalesce(configuration -> 'missions', '{}'::jsonb);
  maximum_concurrent := coalesce((mission_rules ->> 'maximumConcurrentMissions')::integer, 4);

  if new.status in ('assigned','accepted') then
    if tg_op = 'UPDATE'
      and old.status in ('assigned','accepted')
      and old.ambassador_id is not distinct from new.ambassador_id then
      return new;
    end if;
    perform pg_advisory_xact_lock(hashtext(new.tenant_id || ':' || new.organization_id || ':ambassador:' || new.ambassador_id::text)::bigint);
    select count(*) into active_count
    from public.market_os_ambassador_mission_assignments assignment
    where assignment.tenant_id = new.tenant_id
      and assignment.organization_id = new.organization_id
      and assignment.ambassador_id = new.ambassador_id
      and assignment.status in ('assigned','accepted')
      and assignment.archived_at is null
      and assignment.id <> new.id;
    if active_count >= maximum_concurrent then
      raise exception 'Ambassador concurrent mission limit is % under the effective policy', maximum_concurrent using errcode = '23514';
    end if;
  end if;
  return new;
end $$;

