function enterpriseMessage(params: {
  context: string
  purpose: string
  instruction: string
  expectedAction: string
  deadline: string
  closing: string
}) {
  return `Bonjour,

Dans le cadre de ${params.context}, nous vous transmettons la communication suivante.

Objet :
${params.purpose}

Instruction principale :
${params.instruction}

Action attendue :
${params.expectedAction}

Délai ou niveau d’attention :
${params.deadline}

Merci d’accuser réception depuis votre espace personnel ANGELCARE. Si une précision, une contrainte ou un point de blocage existe, merci de laisser un commentaire directement sous ce message afin que l’administration puisse assurer le suivi.

${params.closing}

Cordialement,
ANGELCARE Operations`
}

